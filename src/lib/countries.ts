// Elenco paesi (nomi in italiano + codice ISO 3166-1 alpha-2).
// La bandierina emoji è derivata dal codice via flagEmoji(), così non serve
// mantenere due campi in sync.

export interface Country {
  code: string; // ISO 3166-1 alpha-2
  name: string; // nome in italiano
}

/** Converte un codice ISO-2 nell'emoji bandiera (regional indicator symbols). */
export function flagEmoji(code: string): string {
  return code
    .toUpperCase()
    .replace(/./g, (c) => String.fromCodePoint(127397 + c.charCodeAt(0)));
}

